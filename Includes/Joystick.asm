	* = * "Joystick Routines"

//=====================================================================================================================
// Joystick control to go through screens
//=====================================================================================================================

PlayerJoyControl: {
        lda	$dc00
        lsr
        bcc	jup
        lsr
        bcc	jdown
        lsr
        bcc	jleft
        lsr
        bcc	jright
        lsr
        bcc	jfire
        rts

	jup:
		dec $d001
		rts

	jdown:
		inc $d001
		rts

	jleft:
		dec $d000
		rts

	jright:
		inc $d000

	jfire:
	Exit:
		lda $0
		sta $dc00
		rts

}

